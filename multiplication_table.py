def print_multiplication_table(table, start, end):
    for i in range(start, end + 1):
        print(f"{table} * {i} = {table * i}")


print_multiplication_table(5, 1, 10)
print_multiplication_table(6, 1, 10)
print_multiplication_table(7, 1, 10)
print_multiplication_table(8, 1, 10)
print_multiplication_table(9, 1, 10)
